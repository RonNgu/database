drop database if exists auction;

CREATE SCHEMA IF NOT EXISTS auction;

USE auction;

CREATE TABLE USER (
	Username VARCHAR(15) NOT NULL,
    Password VARCHAR(15) NOT NULL,
    Email VARCHAR(40) NOT NULL,
    Address VARCHAR(30) NOT NULL,
    User_id INT, 
    CONSTRAINT USRPK PRIMARY KEY (User_id)
);

CREATE TABLE EMPLOYEE (
	Salary DECIMAL(10, 2) NOT NULL,
    Position VARCHAR(15) NOT NULL,
    Employee_id INT,
    PRIMARY KEY (Employee_id)
);

CREATE TABLE SELLER (
	Seller_name VARCHAR(15) NOT NULL,
    Seller_id INT,
    CONSTRAINT SELPK PRIMARY KEY (Seller_id)
);

CREATE TABLE ITEM (
	Item_name VARCHAR(15) NOT NULL,
    Seller_id INT, 
    Item_id INT,
    CONSTRAINT ITEMPK PRIMARY KEY (Item_id),
	FOREIGN KEY (Seller_id) REFERENCES SELLER(Seller_id) ON DELETE SET NULL ON UPDATE CASCADE
);

CREATE TABLE AUCTION ( 
	Starting_time DATE NOT NULL,
    Starting_bid INT NOT NULL,
    Time_limit INT NOT NULL,
    Item_id INT,
    Auction_id INT,
    PRIMARY KEY (Auction_id),
	FOREIGN KEY (Item_id) REFERENCES ITEM(Item_id) ON DELETE SET NULL ON UPDATE CASCADE
);

CREATE TABLE BID (
	Bid_amount INT NOT NULL,
    Auction_id INT,
    User_id INT,
    Bid_id INT,
    CONSTRAINT BIDPK PRIMARY KEY (Bid_id),
    CONSTRAINT BIDAUCFK FOREIGN KEY (Auction_id) REFERENCES AUCTION(Auction_id) ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT BIDUSRFK FOREIGN KEY (User_id) REFERENCES USER(User_id) ON DELETE SET NULL ON UPDATE CASCADE
);

CREATE TABLE WORKS_ON (
	Employee_id INT,
    Auction_id INT, 
    Hours INT NOT NULL,
    PRIMARY KEY (Employee_id, Auction_id),
    FOREIGN KEY (Employee_id) REFERENCES EMPLOYEE(Employee_id) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (Auction_id) REFERENCES AUCTION(Auction_id) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE RESULT1 (
	Timestamp DATE NOT NULL, 
    Final_bid INT NOT NULL,
    Result_id INT,
    User_id INT NOT NULL,
    Auction_id INT,
    PRIMARY KEY (Result_id),
    FOREIGN KEY (Auction_id) REFERENCES AUCTION(Auction_id) ON DELETE SET NULL ON UPDATE CASCADE
);

CREATE TABLE RESULT2 (
	Final_bid INT NOT NULL,
    User_id INT NOT NULL,
    Auction_id INT NOT NULL,
    PRIMARY KEY (Auction_id),
    FOREIGN KEY (Auction_id) REFERENCES AUCTION(Auction_id) ON DELETE CASCADE ON UPDATE CASCADE
); 

